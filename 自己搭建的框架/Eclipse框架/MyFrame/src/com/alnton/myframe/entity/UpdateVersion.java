package com.alnton.myframe.entity;

import java.io.Serializable;

/**
 * <版本实体类> 
 * @author  王乾州
 */
public class UpdateVersion implements Serializable
{
    /**
     * 注释内容
     */
    private static final long serialVersionUID = 1459709794737367560L;
    
    /**
     * 最新版本号
     * 这个对应的是versionName
     */
    private String verCode;
    
    /**
     * 最新版本下载url
     */
    private String downUrl;
    
    /**
     * 最新版本描述
     */
    private String verDesc;
    
    /**
     * 是否强制更新
     * 0: 强制更新
     * 1: 不强制更新
     */
    public String downType;

    public String getVerCode()
    {
        return verCode;
    }

    public void setVerCode(String verCode)
    {
        this.verCode = verCode;
    }

    public String getDownUrl()
    {
        return downUrl;
    }

    public void setDownUrl(String downUrl)
    {
        this.downUrl = downUrl;
    }

    public String getVerDesc()
    {
        return verDesc;
    }

    public void setVerDesc(String verDesc)
    {
        this.verDesc = verDesc;
    }

    public String getDownType()
    {
        return downType;
    }

    public void setDownType(String downType)
    {
        this.downType = downType;
    }
}